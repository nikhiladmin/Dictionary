
const dictionary = require('./Dictionary');
const test1= {
    '2019-01-01':100,
    '2019-01-04':115,
}
const test2= {
    '2019-01-10':10,
    '2019-01-11':20,
    '2019-01-13':10,
}
const result1={
    '2019-01-01':100,
    '2019-01-02':105,
    '2019-01-03':110,
    '2019-01-04':115
}
const result2={
    '2019-01-10': 10,
    '2019-01-11': 20,
    '2019-01-12': 15,
    '2019-01-13': 10
  }



  test(`INPUT-{'2019-01-01':100,'2019-01-04':115,}
  OUTPUT-{'2019-01-01':100,'2019-01-02':105,'2019-01-03':110,'2019-01-04':115}`, () => {
    expect(dictionary(test1)).toEqual(result1);
  });

  test(`INPUT-{'2019-01-10':10,'2019-01-11':20,'2019-01-13':10,}
  OUTPUT-{'2019-01-10': 10,'2019-01-11': 20,'2019-01-12': 15,'2019-01-13': 10}`, () => {
    expect(dictionary(test2)).toEqual(result2);
  });